# start is the epoch date to check for
# @a is the array of epoch commit dates
#
# Returns the share of lines from before the cut-off date
sub fromafter {
    my ($after, @a) = (@_);
    my @vals = sort {$b <=> $a} @a;
    my $count;
    for my $i (@vals) {
        if($i > $after) {
            $count++;
        }
    }

    return $count * 100 / scalar(@vals);
}

sub singlefile {
    my ($tag, $f) = @_;
    open(G, "git blame -t --line-porcelain $f $tag|");
    my $author;
    my @stamp;
    while(<G>) {
        if(/^committer-time (.*)/) {
            # store every timestamp
            push @stamp, $1;
        }
    }
    close(G);
    return @stamp;
}

my %cutoff;

# store the cutoff years in epoch seconds
for my $year (2000 .. 2030) {
    $cutoff{$year} = `date +%s -d "$year-01-01"`;
}

sub show {
    my ($tag, $date) = @_;
    my @stamp;
    my @f=`git ls-tree -r --name-only $tag -- src lib include 2>/dev/null`;

    for my $e (@f) {
        chomp $e;
        if(($e =~ /\.[ch]\z/) || ($e =~ /makefile/i)) {
            push @stamp, singlefile($tag, $e);
        }
    }

    printf "$date";

    for (my $i = 2000; $i <= 2026; $i += 2) {
        printf ";%.1f", fromafter($cutoff{$i}, @stamp);
    }
    print "\n";
}

